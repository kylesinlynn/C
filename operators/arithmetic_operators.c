#include <stdio.h>

int main()
{
	/*
	* Fives arithemetic operators
	+ -> addition
	- -> substraction
	* -> multiplication
	/ -> division
	% -> modulo
	*/

	// + -> addition
	printf("3 + 3 = %i\n", 3+3);

	// - -> substraction
	printf("3 - 2 = %i\n", 3-2);
	
	// * -> multiplication
	printf("3 * 2 = %i\n", 3*2);

	// / -> division
	printf("5.0 / 2 = %1.1f\n", 5.0/2);

	// % -> modulo
	printf("5 % 2 = %i\n", 5%2);

	return 0;
}
